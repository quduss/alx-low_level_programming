/**
 * op_add - adds two integers
 * @a: first integer
 * @b: second integer
 * Return: addition of the two integers
 */

int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtracts second integer from first
 * @a: first integer
 * @b: second integer
 * Return: result of the subtraction
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplies two integers
 * @a: first integer
 * @b: second integer
 * Return: product of the two integers
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divides first integer by second
 * @a: first integer
 * @b: second integer
 * Return: result of the division
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - gets the remainder of a / b
 * @a: first integer
 * @b: second integer
 * Return: remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
